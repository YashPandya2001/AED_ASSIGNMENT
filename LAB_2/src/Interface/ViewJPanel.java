    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        
       int selectedrow = vitalTable.getSelectedRow();
       
       if (selectedrow>=0)
       {
           VitalSigns vs=(VitalSigns)vitalTable.getValueAt(selectedrow, 0);
           tempTxtField.setText(String.valueOf(vs.getTemperature()));
           bldPreTxtField.setText(String.valueOf(vs.getBloodPressure()));
           pulseTxtField.setText(String.valueOf(vs.getPulse()));
           dateTxtField.setText(vs.getDate());
       }
       else 
       {
           JOptionPane.showMessageDialog(null,"Please select any row");
       }
    }//GEN-LAST:event_viewBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = vitalTable.getSelectedRow();
        
        if (selectedrow >= 0)
        {
            VitalSigns vs = (VitalSigns)vitalTable.getValueAt(selectedrow, 0);
            vsh.removeVital(vs);
            JOptionPane.showMessageDialog(null,"The row has been deleted");
            populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select any row");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


